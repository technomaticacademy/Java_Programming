package base;

public class PatternPrograms {

	public static void main(String[] args) {
		// Program to Print:
		// ***
		// ***
		// ***
		// ***
		// ***
		for (int row = 1; row <= 5; row++) {
			for (int column = 1; column <= 5; column++) {
				System.out.print("*");
			}
			System.out.println();
		}

	}

}
